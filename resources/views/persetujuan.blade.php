# Persetujuan Pendaftaran Daftar Properti

Dengan mendaftarkan suatu Listing di Daftar Properti, maka pendaftar dan Daftar Properti menyetujui hal-hal yang dicantumkan di dokumen ini.

### Pihak yang Menandatangani

Dengan ini, Pendaftar ("Pihak Pertama") menyetujui ketentuan-ketentuan yang berlaku dalam pendaftaran properti di Daftar Properti ("Pihak Kedua"),
sebagaimana diatur dalam persetujuan ini.

## 1. Pernyataan Pendaftar

Dengan mendaftarkan properti untuk dijual/disewakan di Daftar Properti, Pihak Pertama menyatakan dan menyetujui hal-hal sebagai berikut:

1.1. Pihak Pertama adalah pemilik sah dari properti yang didaftarkan atau adalah agen/perantara yang memiliki hubungan langsung dengan pemilik properti tersebut.

1.2. Informasi yang disampaikan mengenai properti adalah benar, akurat, dan tidak menyesatkan.

1.3. Pihak Pertama setuju bahwa apabila properti laku (terjual atau tersewa), maka Pihak Pertama akan membayar komisi sebesar 0,5% dari harga penjualan (untuk penjualan) atau 1% dari total sewa (untuk penyewaan) kepada Pemasar yang berhasil merujuk properti tersebut, sebagaimana ditentukan oleh Pihak Kedua.

1.4. Pihak Pertama diwajibkan melaporkan kepada Daftar Properti apabila properti yang didaftarkan tidak lagi aktif atau tersedia di pasar.

1.5. Ketika properti laku (terjual atau tersewa), Pihak Pertama berkewajiban untuk melaporkan informasi penutupan transaksi dengan benar agar Daftar Properti dapat secara adil menentukan siapa Pemasar yang menjadi penghubung menggunakan teknologi Referral Tracking. Melaporkan transaksi dengan benar akan memberikan manfaat bagi Pihak Pertama, karena informasi penjualan yang terlapor akan menjadi informasi publik yang dapat meningkatkan nilai properti di masa mendatang.

1.6. Pihak Pertama akan membayar komisi secara langsung kepada Pemasar yang ditentukan oleh Daftar Properti, bukan kepada Daftar Properti. Daftar Properti tetap akan memantau dan menegakkan kewajiban pembayaran ini meskipun pembayaran dilakukan langsung kepada Pemasar.

## 2. Pernyataan Daftar Properti

Pihak Kedua, Daftar Properti, menyatakan dan menyetujui hal-hal sebagai berikut:

2.1. Setiap properti yang didaftarkan akan dilindungi untuk pendaftar pertama. Artinya, jika ada agen atau pemilik lain yang mencoba mendaftarkan properti yang sama, pendaftaran tersebut tidak akan diterima. Hal ini dilakukan agar Pihak Pertama dihargai atas transparansi dan segala pertanyaan mengenai properti tersebut akan diteruskan kepada Pihak Pertama, baik sebagai pemilik maupun agen.

2.2. Pengecualian hanya berlaku apabila ada pengaduan dari pemilik properti terkait kurangnya upaya dari agen untuk mempertahankan kualitas listing.

2.3. Daftar Properti akan memberitahukan seluruh jaringan Pemasar mengenai penambahan atau perubahan pada listing, memastikan bahwa informasi tersebar dengan cepat dan efisien.

2.4. Informasi mengenai listing dan penutupan transaksi akan dipublikasikan untuk memastikan efisiensi pasar. Namun, Daftar Properti menjamin privasi Pihak Pertama dengan tidak menyertakan nomor telepon dalam catatan yang tersedia untuk publik.

## 3. Penandatanganan

Dengan menandatangani perjanjian ini, Pihak Pertama menyetujui semua ketentuan di atas dan menyatakan kesediaan untuk tunduk dan patuh pada perjanjian ini.

